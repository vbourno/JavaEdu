package gr.aueb.cf.ch2;

/**
 * Χρησιμοποιεί την έτοιμη κλάση Math
 */
public class MathApp {

    public static void main(String[] args) {
        System.out.println("Absolute of -5" + Math.abs(-5));
        System.out.println("Το ελάχιστο (min) του 1, 10" + Math.min(1, 10));
        System.out.println("Το μέγιστο (max) του 1, 10" + Math.max(1, 10));
    }
}
